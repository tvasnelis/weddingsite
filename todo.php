            <div class="item active">
              <h4><a href="http://hotelmonteleone.com/entertainment/carousel-bar/" target="_blank">Carousel Bar</a></h4>
              <div class="item-address">
                <p><a href="https://www.google.com/maps/place/Carousel+Bar/@29.954163,-90.068029,15z/data=!4m5!3m4!1s0x0:0xdd909df48675434d!8m2!3d29.954163!4d-90.068029" target="_blank">214 Royal Street</a></p>
                <p>French Quarter</p>
              </div>
              <div class="item-text">
                <p>The iconic Carousel Bar is located inside the Hotel Monteleone and overlooks Royal Street in the French Quarter.  The circular bar, which slowly rotates, is a wonderful place to kill and hour or two enjoying your favorite cocktail and mingling with the other riders.</p>
              </div>
              <p>Signature Cocktail: Vieux Carre</p>
              <p>Tim's Drink: Bourbon Milk Punch</p>
              <p>Kimberly's Drink: Aviation</p>
            </div>

            <div class="item">
              <h4><a href="https://www.arnaudsrestaurant.com/bars/french-75/" target="_blank">Arnaud's French 75</a></h4>
              <div class="item-address">
                <p><a href="https://www.google.com/maps/place/French+75/@29.9557933,-90.0686926,15z/data=!4m5!3m4!1s0x0:0xbbb24fa3f0588a4c!8m2!3d29.9557933!4d-90.0686926"target="_blank">813 Bienville Street</a></p>
                <p>French Quarter</p>
              </div>
              <div class="item-text">
                <p>In a city famous for the availability of alcohol in excess, what often gets forgotten is the historical influence New Orleans has had on cocktails.  This place feels like a time warp to an era we never experienced first hand, where white tuxedoed bartenders serve up some great cocktails.</p>
              </div>
              <p>Signature Cocktail: French 75</p>
              <p>Tim's Drink: Creole</p>
              <p>Kimberly's Drink: French 75</p>
            </div>

            <div class="item">
              <h4><a href="http://curenola.com/" target="_blank">Cure</a></h4>
              <div class="item-address">
                <p><a href="https://www.google.com/maps/place/Cure/@29.9350286,-90.1096536,17z/data=!3m1!4b1!4m5!3m4!1s0x8620a5a9359e0abb:0x971b22a78c122688!8m2!3d29.935024!4d-90.1074596" target="_blank">4905 Freret Street</a></p>
                <p>Uptown</p>
              </div>
              <div class="item-text">
                <p>A bit of a hike from French Quarter but well worth the trip. Housed in a converted firehouse, the beautiful backbar highlights their gigantic bottle collection. Specializing in rum and whiskey, the cocktail list is lengthy, seasonal, and includes a lot of unfamiliar ingredients.  Be adventurous!</p>
              </div>
              <p>Signature Cocktail: Rum Old Fashioned</p>
              <p>Tim's Drink: Cock 'n Bull Special</p>
              <p>Kimberly's Drink: Last Dance</p>
            </div>

            <div class="item">
              <h4><a href="http://hottinbar.com/" target="_blank">Hot Tin</a></h4>
              <div class="item-address">
                <p><a href="https://www.google.com/maps/place/Hot+Tin/@29.9351131,-90.0797176,15z/data=!4m2!3m1!1s0x0:0xca107f3e51a95c55?sa=X&ved=0ahUKEwju__qSuovSAhVBPCYKHWz9BpIQ_BIIhgEwCg" target="_blank">2031 Saint Charles Avenue</a></p>
                <p>Garden District</p>
              </div>
              <div class="item-text">
                <p>Located in the open-air penthouse of the historic Pontchartrain Hotel providing some spectacular views of the Mississippi River and downtown New Orleans.  A great place to check out if the weather is nice.</p>
              </div>
            </div>

            <div class="item">
              <h4><a href="http://www.erinrosebar.com/" target="_blank">Erin Rose</a></h4>
              <div class="item-address">
                <p><a href="https://www.google.com/maps/place/Erin+Rose/@29.9565322,-90.0701209,17z/data=!3m1!4b1!4m5!3m4!1s0x8620a60c2a3b32d3:0x45ea80baa89d884b!8m2!3d29.9565276!4d-90.0679269" target="_blank">811 Conti Street</a></p>
                <p>French Quarter</p>
              </div>
              <div class="item-text">
                <p>A dive bar in the best way possible.  Just a few steps off Bourbon street this Irish pub hosts an ecletic group of locals and tourists.  Hidden in back is Killer Po'Boys, who serve some delicious modern versions of the classic sandwich.</p>
              </div>
              <p>Signature Cocktail: Frozen Irish Coffee</p>
            </div>

